package 合并两个有序数组;

/**
 * @author joy division
 * @date 2020/10/26 21:50
 */
public class Employee {
    private Integer age;
    private Integer id;

    public Employee(Integer id) {
    }

    public Employee(Integer age, Integer id) {
        this.age = age;
        this.id = id;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
